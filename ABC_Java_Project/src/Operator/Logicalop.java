package Operator;

public class Logicalop {

	public static void main(String[] args) {
		
		boolean bool1=false, bool2=false;
		//logical AND
		System.out.println("bool1 && bool2: "+ (bool1 && bool2));
		//logical OR
		System.out.println("bool1 || bool2: "+ (bool1 || bool2));
		//logical not
		System.out.println("!(bool1 && bool2 ): "+(bool1 &&bool2));
		

	}

}
